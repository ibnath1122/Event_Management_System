    public String srch_by_com;

    public void srch_combo_logic() {
        try {
            if (jComboBox2.getSelectedItem().equals("staff_id")) {
                srch_by_com = "staff_id";
            }else if (jComboBox2.getSelectedItem().equals("name")) {
                srch_by_com = "name";
            } else if (jComboBox2.getSelectedItem().equals("email")) {
                srch_by_com = "email";
            } else if (jComboBox2.getSelectedItem().equals("phone_num")) {
                srch_by_com = "phone_num";
            } else if (jComboBox2.getSelectedItem().equals("address")) {
                srch_by_com = "address";
            } else if (jComboBox2.getSelectedItem().equals("dob")) {
                srch_by_com = "dob";
            } else if (jComboBox2.getSelectedItem().equals("gender")) {
                srch_by_com = "gender";
            } else if (jComboBox2.getSelectedItem().equals("salary")) {
                srch_by_com = "salary";
            } else if (jComboBox2.getSelectedItem().equals("joining_date")) {
                srch_by_com = "joining_date";
            } else if (jComboBox2.getSelectedItem().equals("position")) {
                srch_by_com = "position";
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }


