    public ArrayList<Staffinfo> userList() {
        ArrayList<Staffinfo> usersList = new ArrayList<>();
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            //Connection connection = DriverManager.getConnection( "jdbc:sqlserver://localhost:1433;databaseName=testdb;selectMethod=cursor", "sa", "123456");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=event_management; user=sa; password=123456";
            Connection con = DriverManager.getConnection(url);
            String query1 = "SELECT * FROM staff_details";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query1);
            Staffinfo staff;
            while (rs.next()) {

                staff = new Staffinfo(rs.getInt("staff_id"), rs.getString("name"), rs.getString("email"), rs.getString("phone_num"),
                        rs.getString("address"), rs.getString("dob"), rs.getString("gender"), rs.getInt("salary"),
                        rs.getString("joining_date"), rs.getString("position"));

                usersList.add(staff);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return usersList;

    }

    public void show_staff() {
        ArrayList<Staffinfo> list = userList();
        DefaultTableModel model = (DefaultTableModel) table1.getModel();
        Object[] row = new Object[10];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getstaff_id();
            row[1] = list.get(i).getname();
            row[2] = list.get(i).getemail();
            row[3] = list.get(i).getphone_num();
            row[4] = list.get(i).getaddress();
            row[5] = list.get(i).getdob();
            row[6] = list.get(i).getgender();
            row[7] = list.get(i).getsalary();
            row[8] = list.get(i).getjoining_date();
            row[9] = list.get(i).getposition();
            model.addRow(row);

        }
    }

