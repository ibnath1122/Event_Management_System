    private void table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1MouseClicked
        // TODO add your handling code here:
        int i = table1.getSelectedRow();

        TableModel model = table1.getModel();
        jTextField1.setText(model.getValueAt(i, 1).toString());
        jTextField2.setText(model.getValueAt(i, 2).toString());
        jTextField3.setText(model.getValueAt(i, 3).toString());
        jTextField4.setText(model.getValueAt(i, 4).toString());

        //jDateChooser1.setDate(rs.getDate("dob"));
        String sex = model.getValueAt(i, 6).toString();
        {
            if (sex.equals("male")) {
                jRadioButton1.setSelected(true);
            } else {
                jRadioButton2.setSelected(true);
            }
        }

        jTextField5.setText(model.getValueAt(i, 7).toString());

        String pos = model.getValueAt(i, 9).toString();
        switch (pos) {
            case "manager":
                jComboBox1.setSelectedIndex(0);
                break;

            case "employee":
                jComboBox1.setSelectedIndex(1);
                break;

            case "staff":
                jComboBox1.setSelectedIndex(2);
                break;
        }

        try {
            int srow = table1.getSelectedRow();
            Date dob = new SimpleDateFormat("yyyy-MM-dd").parse((String) model.getValueAt(srow, 5));
            jDateChooser1.setDate(dob);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

        try {
            int srow1 = table1.getSelectedRow();
            Date joining_date = new SimpleDateFormat("yyyy-MM-dd").parse((String) model.getValueAt(srow1, 8));
            jDateChooser2.setDate(joining_date);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

        


    }//GEN-LAST:event_table1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            //Connection connection = DriverManager.getConnection( "jdbc:sqlserver://localhost:1433;databaseName=testdb;selectMethod=cursor", "sa", "123456");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=event_management; user=sa; password=123456";
            Connection con = DriverManager.getConnection(url);
            int row = table1.getSelectedRow();
            String value = (table1.getModel().getValueAt(row, 0).toString());
            String query = "Update staff_details set name=?, email=?,phone_num=?,address=?, dob=?, gender=?,salary=?, joining_date=?,position=? where staff_id=" + value;

            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jTextField2.getText());
            pst.setString(3, jTextField3.getText());
            pst.setString(4, jTextField4.getText());

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String dob = sdf.format(jDateChooser1.getDate());
            pst.setString(5, dob);

            if (jRadioButton1.isSelected()) {
                gender = "male";
            }
            if (jRadioButton2.isSelected()) {
                gender = "female";
            }
            pst.setString(6, gender);

            pst.setString(7, jTextField5.getText());

            SimpleDateFormat sdf1 = new SimpleDateFormat("yyyy-MM-dd");
            String joining_date = sdf1.format(jDateChooser2.getDate());
            pst.setString(8, joining_date);

            String pos;
            pos = (String) jComboBox1.getSelectedItem().toString();
            pst.setString(9, pos);


            pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel) table1.getModel();
            model.setRowCount(0);
            show_staff();
            JOptionPane.showMessageDialog(null, "Update Successfull");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);

        }

    }//GEN-LAST:event_jButton3ActionPerformed

